    private void txtContraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtContraActionPerformed
        // TODO add your handling code here:
        
        int pass;
        String contra;
        contra = new String(txtContra.getPassword());
        pass = Integer.parseInt(contra);
        if(contra.equals("12345")){
            user.setContra(pass);
        }else{
            JOptionPane.showMessageDialog(this,"Contrase√±a incorrecta","Mensaje Error",JOptionPane.ERROR_MESSAGE);
            this.txtContra.setText("");
            this.txtContra.requestFocus();
        }
        
    }//GEN-LAST:event_txtContraActionPerformed

    private void txtUsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(Character.isDigit(c)){
            evt.consume();
        }else if( c == KeyEvent.VK_ENTER){
            this.txtContra.requestFocus();
        }
    }//GEN-LAST:event_txtUsuarioKeyTyped

